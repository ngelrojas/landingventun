<h1 class="font-weight-bold">¿Qué Opciones Tengo?</h1>
<p>
Si tienes alguna pregunta u objeción en relación con el modo en que recabamos y tratamos tu
información personal, por favor, ponte en contacto al correo info@ventun.com. Muchos de nuestros
Servicios de Ventun también incluyen opciones que te permiten configurar el modo en que se está
utilizando tu información personal.
</p>

<ul>
<li> Como hemos indicado anteriormente, puedes elegir no suministrar cierta información personal,
pero en ese caso podrías perder la posibilidad de aprovechar muchas de las ventajas de los
Servicios de Ventun.</li>
<li> Si no deseas que te enviemos correos electrónicos u otras comunicaciones, te rogamos que
ajustes tus suscripciones de correo electrónico y preferencias de notificación poniéndote en
contacto al correo <a href="">info@ventun.com.</a></li>
<li> Cuando prestes tu consentimiento al tratamiento de tu información personal para una finalidad
determinada, podrás revocarlo en cualquier momento y dejaremos de tratar tu información
personal con esa finalidad.</li>
</ul>

<p>
Además, conforme a la legislación aplicable, tienes derecho a acceder, rectificar y suprimir tu
información personal, así como a solicitar la portabilidad de los datos. También podrás oponerte aque tratemos tu información personal o solicitarnos que limitemos dicho tratamiento en determinadas
circunstancias. Para ello, por favor, ponte en contacto al correo <a href="">info@ventun.com</a>.
</p>

<h1 class="font-weight-bold"> ¿Está Permitido el Uso de los Servicios de Ventun a los
Menores de Edad?</h1>

<p>
Los menores de edad no están autorizados a comprar productos de Ventun Bolivia. Los productos
para menores que vendemos deben ser adquiridos por adultos. Los menores de 18 años sólo podrán
utilizar los Servicios de Ventun bajo la supervisión de un padre o un tutor.
</p>

<h1 class="font-weight-bold"> ¿Cuánto tiempo conservamos tu información personal?</h1>
<p>
Conservamos tu información personal a fin de que puedas utilizar los Servicios de Ventun de forma
continuada y durante el tiempo necesario para cumplir los fines descritos en este Aviso de Privacidad,
según resulte exigible conforme a la legislación (por ejemplo, con fines fiscales y contables), o de
cualquier otro modo que te hayamos comunicado. Por ejemplo, conservamos tu historial de
operaciones para que puedas revisar compras pasadas (y repetir pedidos, si así lo deseas) y las
direcciones a las que has enviado pedidos, y para mejorar la relevancia de los productos y contenido
que recomendamos.
</p>

<h1 class="font-weight-bold"> Contactos, Notificaciones y Revisiones</h1>
<p>
Si tienes cualquier duda en relación con la privacidad en Ventun Bolivia o quieres ponerte en contacto
con alguno de nuestros responsables del tratamiento, te rogamos nos envíes un correo
electrónico detallando tu consulta y trataremos de resolverla. Asimismo, se puede contactar con el
delegado de protección de datos de dichos responsables del tratamiento a través del correo
info@ventun.com.
Nuestras actividades evolucionan constantemente y nuestro Aviso de Privacidad también lo hará. Te
recomendamos que visites nuestra página aplicación móvil frecuentemente para consultar los
cambios más recientes. A menos que se indique lo contrario, nuestro Aviso de Privacidad actual será
aplicable para toda la información personal que tratamos referente a ti y a tu cuenta. De todos modos,
somos fieles a nuestra palabra y en ningún caso modificaríamos sustancialmente nuestras políticas
ni prácticas para hacerlas menos eficaces en la protección de la información personal de nuestros
clientes recogida en el pasado, a menos que contásemos con el consentimiento previo de los clientes
afectados.
</p>


