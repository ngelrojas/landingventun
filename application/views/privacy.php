aplicación móvil (incluyendo fecha y hora), productos y/o contenido que ha visitado o que ha
buscado, tiempos de respuesta de la página, errores de descarga, información sobre la
interacción con la página (clics, desplazamiento por la página o número de veces que pasa el
ratón por encima de determinados ítems);</li>
<li> números de teléfono utilizados para llamar a nuestro servicio de atención al cliente.</li>
</ul>

<p>
También podremos utilizar identificadores de dispositivos y otras tecnologías en dispositivos,
