<p class="font-weight-bold">
Información automática:
</p>

<p>
Estos son algunos ejemplos de la información que recogemos y analizamos:
</p>

<ul>
<li> la dirección IP de Internet que utilizas para conectarte a Internet con tu ordenador u otro
dispositivo;</li>
<li> nombre de usuario, dirección de correo electrónico y contraseña de tu cuenta;</li>
<li> información sobre tu ordenador, dispositivo y tu conexión a Internet como la aplicación del
