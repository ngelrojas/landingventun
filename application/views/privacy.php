<h1 class="font-weight-bold"> ¿Qué Información Personal de Clientes Recaba Ventun?</h1>
<p>
Recabamos tu información personal para prestar y mejorar de forma continua nuestros productos y
servicios.
</p>
<p>
Estos son los tipos de información que recabamos:
</p>
<ul>
<li> <span class="font-weight-bold">Información que nos Facilitas:</span> <span> recibimos y almacenamos toda la información que nos facilitas
en relación con los Servicios de Ventun.<span></li>
</ul>
<ul>
<li> <span class="font-weight-bold">Información Automática:</span> <span> cada vez que utilizas los Servicios de Ventun, recibimos y
almacenamos de forma automática ciertos tipos de información, tales como información sobre
el uso, incluyendo tu interacción con el contenido y los servicios disponibles a través de los
Servicios de Ventun.<span></li>
</ul>
<ul>
<li> <span class="font-weight-bold">Información Procedente de Otras Fuentes:</span> <span> Es posible que recibamos información sobre ti
de otras fuentes.<span></li>
</ul>
<h1 class="font-weight-bold"> ¿Con qué Finalidades Trata Tu Información Personal
Ventun?</h1>
<p> Tratamos tu información personal para operar, prestar y mejorar los Servicios de Ventun que
ofrecemos a nuestros clientes. Entre estas finalidades se incluyen:</p>

<ul>
<li> <span class="font-weight-bold">Compra y entrega de productos y servicios.</span> <span> Utilizamos tu información personal para aceptar
y gestionar pedidos, entregar productos y servicios, procesar pagos y comunicarnos contigo en
relación con pedidos, productos, servicios y ofertas promocionales.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Prestación, resolución de problemas y mejora de los Servicios de Ventun.</span> <span> Utilizamos tu
información personal para aportar funcionalidad, analizar el rendimiento, resolver errores y
mejorar la usabilidad y efectividad de los Servicios de Ventun.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Recomendaciones y personalización.</span> <span> Utilizamos tu información personal para recomendar
funcionalidades, productos y servicios que puedan interesarte, identificar tus preferencias y
personalizar tu experiencia con los Servicios de Ventun.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Prestación de servicios de voz.</span> <span> Cuando utilizas los servicios de voz, tratamos la entrada de
voz y otra información personal para dar respuesta a tus peticiones, prestarte el servicio
solicitado y mejorar los Servicios de Ventun.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Cumplimiento de obligaciones legales.</span> <span> En algunos casos, tenemos la obligación legal de
recabar y tratar tu información personal. Por ejemplo, recabamos información de terceros
vendedores sobre el lugar de establecimiento y la cuenta bancaria para verificación de identidad,
entre otras finalidades.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Comunicarnos contigo.</span> <span> Utilizamos tu información personal para comunicarnos contigo en
relación con los Servicios de Ventun a través de diferentes canales (por ejemplo, por teléfono,
correo electrónico, chat).<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Publicidad.</span> <span> Utilizamos información como tus interacciones con sitios, contenido o servicios de
Ventun para mostrar anuncios basados en funcionalidades, productos y servicios que puedan
interesarte. No utilizamos tu información personal para mostrarte anuncios basados en tus
intereses.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Prevención del Fraude y Riesgos Crediticios.</span> <span> Tratamos información personal para prevenir
y detectar el fraude y el abuso con el fin de proteger la seguridad de nuestros clientes, Ventun
y otros. También podemos utilizar métodos de calificación para evaluar y gestionar los riesgos
crediticios.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Finalidades para las que recabamos tu consentimiento.</span> <span> También podemos solicitar tu
consentimiento para tratar tu información personal con una finalidad concreta que te
comunicaremos. Cuando prestes tu consentimiento para que tratemos tu información personal
para una finalidad determinada, podrás revocarlo en cualquier momento y dejaremos de tratar
tu información con esa finalidad.<span></li>
</ul>

<h1 class="font-weight-bold"> ¿Comparte Ventun Bolivia tu Información Personal?</h1>
<p>
La información relativa a nuestros clientes es una parte fundamental de nuestro negocio y no
participamos en actividades de venta de la información personal de nuestros clientes a terceros.
Ventun Bolivia sólo comparte la información personal de sus clientes del modo descrito a
continuación, con Ventun y con filiales bajo el control de Ventun que o bien estén sujetas al presente
Aviso de Privacidad o sigan prácticas al menos tan protectoras como las descritas en este Aviso de
Privacidad.
</p>

<ul>
<li> <span class="font-weight-bold">Operaciones con Terceros:</span> <span> Ponemos a tu disposición servicios, productos, proporcionadas
por terceros para su uso a través de los Servicios de Ventun. Por ejemplo, puedes encargar
productos de terceros vendedores a través de nuestras páginas aplicación móvil de venta
minorista y mayorista. Conocerás cuando haya terceros involucrados en tus operaciones y
compartamos tu información personal relacionada con esas operaciones con dichos terceros.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Terceros Proveedores de Servicios:</span> <span> Contratamos a otras sociedades y personas para realizar
ciertas funciones por nuestra cuenta. Algunas de las funciones que realizan pueden ser: ejecutar
pedidos de productos y servicios, enviar paquetes, enviar correos postales y electrónicos,
eliminar información repetida de las listas de clientes, realizar análisis de datos, ofrecer
asistencia de marketing, ofrecer resultados de búsquedas y enlaces (incluidos enlaces y listados
de pago), procesar pagos, transmitir contenidos, calificar riesgos crediticios y ofrecer servicio de
atención al cliente. Estos terceros proveedores de servicios tendrán acceso a la información
personal necesaria para realizar sus funciones, pero no podrán utilizarla para otros fines.
Además, deberán tratar la información personal de conformidad con el presente Aviso de
Privacidad y la legislación aplicable en materia de protección de datos.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Transmisiones de Sociedades:</span> <span> medida que desarrollamos nuestro negocio, podemos
comprar o vender otras sociedades o servicios. En dichas transacciones, uno de los activos de
las sociedades transferidas suele ser la información personal de los clientes; dicha información
estará en todo caso sujeta a los compromisos adquiridos en los Avisos de Privacidad
preexistentes (a menos, claro está, que el cliente lo autorice de otro modo). Por supuesto, en elimprobable caso de que Ventun o la mayor parte de sus activos sean adquiridos por un tercero,
la información personal de los clientes sería uno de los activos transferidos.<span></li>
</ul>

<ul>
<li> <span class="font-weight-bold">Protección de Ventun Bolivia y Otros:</span> <span> Divulgamos información personal y datos de cuentas
de nuestros clientes cuando creemos que su divulgación es necesaria para el cumplimiento de
la ley, para hacer cumplir o aplicar las Condiciones de Uso y Venta y otros acuerdos o para
proteger los derechos, la propiedad, o seguridad de Ventun Bolivia, sus usuarios, o terceros. Lo
anterior incluye el intercambio de información con otras sociedades y organizaciones para la
protección contra el fraude y la reducción del riesgo de crédito.<span></li>
</ul>

<p>
Aparte de lo establecido anteriormente, se te informará en el caso de que cualquier información sobre
ti se comparta con terceras partes, al objeto de que tengas la oportunidad de elegir que no se
comparta tu información.
</p>

<h1 class="font-weight-bold"> ¿Está a Salvo mi Información Personal?</h1>
<p>
Diseñamos nuestros sistemas y dispositivos teniendo en cuenta tu seguridad y privacidad.
</p>

<ul>
<li> Contamos con sistemas de seguridad físicos, electrónicos y procedimentales en relación con la
recogida, almacenamiento, y divulgación de información personal del cliente. Nuestros
procedimientos de seguridad implican que en ocasiones podamos solicitarte pruebas de
identidad antes de facilitarte información confidencial.</li>
<li> Nuestros dispositivos ofrecen funcionalidades de seguridad que los protegen frente a accesos
no autorizados y pérdidas de datos. Puedes controlar esas funcionalidades y configurarlas en
función de tus necesidades.</li>
<li> Es importante que te protejas contra el acceso no autorizado de terceros a tu contraseña y a tus
ordenadores, dispositivos y aplicaciones. Comprueba que cierras tu sesión al acabar siempre
que estés utilizando un ordenador compartido.</li>
</ul>

