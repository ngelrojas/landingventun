<h1 class="font-weight-bold" id="cond-venta"> Condiciones de venta</h1>
<p>
Las presentes Condiciones de Venta se aplican a la venta de productos por parte de terceros, que
usan la plataforma de Ventun para ofrecer sus productos y servicios
</p>

<p class="font-weight-bold">
Te rogamos que leas detenidamente las presentes condiciones antes de cursar tu pedido a
Ventun. Al cursar tu pedido a Ventun, aceptas quedar vinculado por las presentes
condiciones.
</p>

<h4 class="font-weight-bold"> 1. Nuestro contrato</h4>
<p>
Cuando realices tu pedido, te enviaremos un mensaje confirmando la recepción de tu pedido. Si
estuvieras utilizando ciertos Servicios de Ventun (e.g., aplicaciones móviles de Ventun), la
confirmación de tu pedido podría publicarse en el Servicio de mensajería de la aplicación móvil.
Asimismo, te informaremos sobre el envío de tus productos.
Aceptas recibir facturas de tus compras electrónicamente. Las facturas electrónicas serán facilitadas
por el proveedor o tercero.
</p>

<h4 class="font-weight-bold"> 2. Derecho de devolución voluntaria y garantía legal de conformidad</h4>
<p>
Ventun no se responsabiliza por la devolución y/o garantía de los productos comercializados en la
plataforma, debiendo las partes solucionar sus controversias por los medios legales
correspondientes.
</p>

<h4 class="font-weight-bold"> 3. Precios y disponibilidad</h4>
<p>
Todos los precios incluyen los impuestos legalmente aplicables. Los métodos de pagos realizados a
Ventun, son:
<ul>
<li> 1. Transferencia Bancaria</li>
<li > 2. Pago al contado o en efectivo contra entrega.</li>
</ul>
</p>
<p>
Incluimos en la aplicación móvil información sobre la disponibilidad de los productos que ofrecen los
usuarios, detallándolo en la página de información de cada uno de los productos. No es posible
ofrecer información más precisa sobre la disponibilidad de un producto, más allá de la que
mostramos en la página de información del producto o en otros apartados de la aplicación móvil.
Cuando se procese tu pedido, el proveedor es responsable de la entrega de los productos que
solicitaste y al momento de recibirlo tienes la decisión de aceptarlo o hacer los reclamos
correspondientes.
Ten en cuenta que, en algunos casos, no nos es posible confirmar una fecha de entrega concreta
cuando realizas tu pedido. En estos supuestos, te facilitaremos nuestra mejor estimación,
normalmente indicando un rango de días. Además, podrás consultar el estado de tu pedido en
cualquier momento a través del Menú en el apartado Pedidos de la aplicación móvil.
</p>
<h4 class="font-weight-bold"> 4. Información del producto</h4>
<p>
A menos que se indique expresamente lo contrario, Ventun no es el fabricante de los productos
vendidos en la aplicación móvil. Si bien nos esforzamos para que la información que aparece en
nuestra aplicación móvil sea correcta, en ocasiones el embalaje y los materiales de los productos
pueden contener información adicional o distinta de la que aparece en nuestra aplicación móvil. Los
componentes también pueden cambiar. Te recomendamos que no te detengas a leer solamente la
información disponible en nuestra aplicación móvil si no que, antes de su utilización, leas también
detenidamente el etiquetado, las advertencias e instrucciones que acompañen al producto.
</p>

<h4 class="font-weight-bold"> 5. Información Aduanera</h4>
<p>
Cuando realizas un pedido de productos en Ventun para su entrega en otro país, podrías quedar
obligado a abonar derechos e impuestos de importación, los cuales te serán cobrados en el momento
en el que el paquete llegue a su destino. Cualquier importe adicional debido al despacho aduanero
correrá de tu cuenta. Ventun no tiene control alguno sobre tales importes. Las políticas aduaneras
varían significativamente de un país a otro por lo que, para mayor información, te rogamos consultes
con la oficina aduanera correspondiente. Recuerda, además, que cuando realizas un pedido a
Ventun formalmente la ley te considera como importador, y deberás por ello cumplir todas las leyesy reglamentos aplicables en el país en el que recibes el producto. Tu privacidad es una cuestión de
gran importancia para nosotros. Queremos que nuestros clientes internacionales sean conscientes
de que los envíos internacionales están sujetos a inspección y apertura por parte de las autoridades
aduaneras.
</p>

<h4 class="font-weight-bold"> 7. Nuestra responsabilidad</h4>
<p>
Ni Ventun ni las sociedades de su grupo serán responsable de (i) cualesquiera pérdidas que no
fueran atribuibles a incumplimiento alguno por su parte, (ii) pérdidas empresariales (incluyendo lucro
cesante, de ingresos, de contratos, de ahorros previstos, de datos, pérdida del fondo de comercio o
gastos innecesarios incurridos), o de (iii) cualesquiera pérdidas indirectas o de carácter
consecuencial que no fueran razonablemente previsibles por ambas partes en el momento en que
se formalizó el contrato de compraventa de los productos entre ambas partes.
La legislación de algunos países pudiera no permitir alguno o la totalidad de los límites de
responsabilidad previstos anteriormente. En caso de que dicha legislación te fuera de aplicación,
alguno o la totalidad de dichos límites pudieran no ser aplicables. Asimismo dicha legislación pudiera
conferirte derechos adicionales a los aquí previstos.
Asimismo Ventun no será responsable de ninguna demora o incumplimiento de sus obligaciones
derivadas de las presentes condiciones si dicha demora o incumplimiento fuera atribuible a
circunstancias ajenas a nuestro control razonable.
Los términos y condiciones de la garantía que Ventun ofrece sobre sus productos varían en función
de la persona que realiza la compra: un "consumidor" o bien otra persona que "actúe con fines que
entran en el marco de su actividad profesional".
De forma adicional, podrías tener derecho a cualquier otra garantía (la Garantía Comercial) que en
tu caso pudiera otorgar el fabricante o proveedor. Generalmente el nombre de la entidad que otorga
la Garantía Comercial, así como los términos y condiciones de la misma figura en la documentación
incluida en el embalaje del producto, que te invitamos a leer y conservar en tu poder.
Ventun no será responsable del cumplimiento de las obligaciones previstas en la Garantía Comercial
ofrecida por el proveedor.
</p>

<h4 class="font-weight-bold"> 8. Ley aplicable</h4>
<p>
Las presentes condiciones se regirán e interpretarán de conformidad con las leyes de Bolivia (a
excepción de sus disposiciones sobre conflicto de leyes), excluyéndose expresamente la aplicación
de la Convención de las Naciones Unidas sobre los Contratos de Compraventa Internacional de
Mercaderías. Ambas partes acordamos someternos a la jurisdicción no exclusiva de los tribunales
de Bolivia, lo que significa que podrás reclamar tus derechos como consumidor en relación con las
presentes Condiciones de Venta tanto en Bolivia.
</p>

<h4 class="font-weight-bold"> 9. Modificación de las condiciones de venta</h4>
<p>
Nos reservamos el derecho a realizar cambios a nuestra aplicación móvil, a nuestras políticas y a
nuestros términos y condiciones, incluyendo a las presentes Condiciones de Venta, en cualquier
momento. Quedarás sujeto a los términos y condiciones, políticas y Condiciones de Venta en vigor
en el momento en que realices tu pedido, a menos que, por ley o por requerimiento de las autoridades
públicas, se deba efectuar un cambio a dichos términos y condiciones, políticas o a las presentes
Condiciones de Venta (en cuyo caso, tales cambios podrían resultar aplicables a cualesquiera
pedidos que hubieras realizado anteriormente). Si alguna de las presentes condiciones fuera
declarada inválida, nula o por cualquier causa ineficaz, dicha condición se entenderá excluida sin
que dicha declaración pueda afectar a la validez ni a la exigibilidad del resto de condiciones.
